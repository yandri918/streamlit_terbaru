def get_feed_by_category(category: str):
    """Get all feeds in a specific category"""
    return {name: data for name, data in FEED_DATABASE.items() 
            if data['category'].startswith(category)}

def search_feeds(query: str):
    """Search feeds by name"""
    query_lower = query.lower()
    return {name: data for name, data in FEED_DATABASE.items() 
            if query_lower in name.lower()}

def get_all_categories():
    """Get list of all feed categories"""
    categories = set()
    for data in FEED_DATABASE.values():
        # Extract main category (before hyphen)
        main_cat = data['category'].split(' - ')[0] if ' - ' in data['category'] else data['category']
        categories.add(main_cat)
    return sorted(list(categories))

def compare_feeds(feed_names: list, nutrients: list):
    """Compare nutritional content of multiple feeds"""
    comparison = {}
    for feed_name in feed_names:
        if feed_name in FEED_DATABASE:
            comparison[feed_name] = {
                nutrient: FEED_DATABASE[feed_name].get(nutrient, 0)
                for nutrient in nutrients
            }
    return comparison
